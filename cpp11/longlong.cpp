#include <iostream>

using namespace std;

int main() {
    int a = 100;
    cout << "int size is " << sizeof(a) << endl;

    long b = 100;
    cout << "long size is " << sizeof(b) << endl;

    long long c = 100;
    cout << "long long size is " << sizeof(c) << endl;
}